public class CalculatorGalnykin {
    public int sub(int a, int b) {
        return a + b;
    }

    public int minus(int a, int b) {
        return a - b;
    }

    public int multiply(int a, int b) {
        return a * b;
    }

    public double devide(int a, int b) {
        return a / b;
    }
}
