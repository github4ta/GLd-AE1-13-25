public class CalculatorLapitskaya {
    public int AddNumbers(int a, int b) {
        return a + b;
    }

    public int SubstractNumbers(int a, int b) {
        return a - b;
    }

    public double DivideNumbers(int a, int b) {
        return a / b;
    }

    public int MultipleNumbers(int a, int b) {
        return a * b;
    }
}
